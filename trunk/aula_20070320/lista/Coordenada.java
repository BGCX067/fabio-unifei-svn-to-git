package lista;

public class Coordenada {
	
	private int x, y;
	
/*************************************************************************************/
	
	public Coordenada(int temp_x, int temp_y){
		x = temp_x;
		y = temp_y;
	}
	
/*************************************************************************************/
	
	public int Coordenada_x(){
		return x;
	}
	
/*************************************************************************************/
	
	public int Coordenada_y(){
		return y;
	}	
	
/*************************************************************************************/
	
}
